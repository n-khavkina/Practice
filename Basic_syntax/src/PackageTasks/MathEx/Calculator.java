package PackageTasks.MathEx;

public class Calculator {
    int add(int a, int b) {
        int res = a + b;
        return res;
    }

    int subtract(int a, int b) {
        int res = a - b;
        return res;
    }

    int multiply(int a, int b) {
        int res = a * b;
        return res;
    }

    int divide(int a, int b) {
        int res = a / b;
        return res;
    }
}
